    <h1>Tentang Dinas Tenaga Kerja</h1>

    <p><strong>Dinas Tenaga Kerja</strong> adalah instansi pemerintah daerah yang bertanggung jawab dalam mengatur dan mengelola urusan ketenagakerjaan, baik di tingkat provinsi maupun kabupaten/kota. Dinas ini memiliki peran penting dalam menciptakan kondisi ketenagakerjaan yang kondusif, meningkatkan kompetensi tenaga kerja, serta menjaga hubungan industrial yang harmonis antara pekerja dan pengusaha.</p>

    <h2>Visi</h2>
    <p>Terwujudnya tenaga kerja yang kompeten, produktif, dan sejahtera melalui layanan ketenagakerjaan yang profesional dan berkelanjutan.</p>

    <h2>Misi</h2>
    <ul>
      <li>Meningkatkan kualitas dan kompetensi tenaga kerja melalui pelatihan kerja.</li>
      <li>Memfasilitasi penempatan kerja yang adil dan merata.</li>
      <li>Menjamin perlindungan dan hak-hak tenaga kerja.</li>
      <li>Memperkuat hubungan industrial yang harmonis dan dinamis.</li>
    </ul>

    <h2>Tugas Pokok</h2>
    <p>Menyiapkan, merumuskan, dan melaksanakan kebijakan teknis di bidang ketenagakerjaan, termasuk pelatihan, penempatan kerja, pengawasan hubungan industrial, dan perlindungan tenaga kerja.</p>

    <h2>Fungsi Utama</h2>
    <ul>
      <li>Perencanaan dan pengembangan ketenagakerjaan.</li>
      <li>Penyelenggaraan pelatihan kerja dan sertifikasi kompetensi.</li>
      <li>Pelayanan penempatan tenaga kerja dan bursa kerja.</li>
      <li>Pengawasan norma ketenagakerjaan dan hubungan industrial.</li>
      <li>Penyelesaian perselisihan hubungan kerja dan pengaduan tenaga kerja.</li>
    </ul>
